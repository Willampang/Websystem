<h2>The Object of game</h2>
<h3><p>Snooker goal is to strike the white cue ball so that it hits the object balls in turn and causes them to fall 
    into one of the games six pockets. You can earn points if you legally pocket balls or when your opponent commits a foul. 
    The points will be calculated as explained rules above. And the player who scores more points wins 
    the frame, and the first person to win a predetermined number of frames wins the match.</p>
</h3>

<h2>Illegal action</h2>
<nav>
    <ul>
        <h3>
        <li>Failing to hit any ball with the cue ball.</li>
        <li>Potting the cue ball.</li>
        <li>Potting a red ball before potting a colored ball.</li>
        <li>Not hitting the correct ball first.</li>
        <li>Touching any ball with anything other than the cue tip.</li>
        <li>Striking the cue ball more than once in a single stroke.</li>
        <li>Failing to hit a rail or a ball after contact.</li>
        <li>Failing to declare a free ball if the opponent commits 
            a foul and leaves no clear shot on the next ball.</li>
        </h3>
    </ul>
</nav>

<h2>End Of Game</h2>
<h3><p>A frame ends when all balls have been potted. 
The player with the most points at the end of a frame wins that frame.</p></h3>
